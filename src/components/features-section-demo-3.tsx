export const Globe = ({ className }: { className?: string }) => {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    let phi = 0;

    if (!canvasRef.current) return;

    const globe = createGlobe(canvasRef.current, {
      devicePixelRatio: 2,
      width: 600 * 2,
      height: 600 * 2,
      phi: 0,
      theta: 0,
      dark: 1,
      diffuse: 1.2,
      mapSamples: 16000,
      mapBrightness: 6,
      baseColor: [0.3, 0.3, 0.3],
      markerColor: [0.1, 0.8, 1],
      glowColor: [1, 1, 1],
      markers: [
        // Pontos principais do Brasil
        { location: [-15.7942, -47.8822], size: 0.08 }, // Brasília
        { location: [-23.5505, -46.6333], size: 0.1 }, // São Paulo
        { location: [-22.9068, -43.1729], size: 0.09 }, // Rio de Janeiro
        { location: [-12.9714, -38.5014], size: 0.07 }, // Salvador
        { location: [-25.4284, -49.2733], size: 0.06 }, // Curitiba
        { location: [-30.0346, -51.2177], size: 0.07 }, // Porto Alegre
        { location: [-19.9167, -43.9345], size: 0.06 }, // Belo Horizonte
        { location: [-8.0476, -34.8770], size: 0.06 }, // Recife
        { location: [-3.7319, -38.5267], size: 0.06 }, // Fortaleza
        { location: [-1.4558, -48.5044], size: 0.05 }, // Belém
        { location: [-20.4697, -54.6201], size: 0.05 }, // Campo Grande
        { location: [-15.6014, -56.0979], size: 0.05 }, // Cuiabá
      ],
      onRender: (state) => {
        // Called on every animation frame.
        // `state` will be an empty object, return updated params.
        state.phi = phi;
        phi += 0.01;
      },
    });

    return () => {
      globe.destroy();
    };
  }, []);

  return (
    <canvas
      ref={canvasRef}
      style={{ width: 600, height: 600, maxWidth: "100%", aspectRatio: 1 }}
      className={className}
    />
  );
};
